package main

import "fmt"

func main() {
	a := 7                // 정수 7은 비트로 00000111 입니다.
	b := a << 2           // 00000111 인 a의 비트를 왼쪽으로 2번 이동합니다.
	fmt.Printf("%08b", b) // 비트로 확인하면 00011100이 됩니다. 정수로는 28입니다.

	a = 112               // 정수 112는 비트로 01110000 입니다.
	b = a >> 3            // 01110000인 비트를 오른쪽으로 3번 이동합니다.
	fmt.Printf("%08b", b) // 비트로 확인하면 00001110이 되고 정수로는 14입니다.
}
